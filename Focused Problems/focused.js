function isPalindrome(str) {
  let mid = str.length / 2
  let j = str.length - 1
  while (j <= mid) {
    for (let i = 0; i <= mid; i++) {
      if (str[i] !== str[j]) {
        return false
      }
      j--;
    }
  }
  return true
}

